function date(date)
		{
			var mydate = new Date(date);
			var d = mydate.getDate();
			d = d <= 9 ? '0' + d : d;
			var month = ["January", "February", "March", "April", "May", "June",
			"July", "August", "September", "October", "November", "December"][mydate.getMonth()];
			var str = d+' ' + month + ' ' + mydate.getFullYear();
			return str;
		}

